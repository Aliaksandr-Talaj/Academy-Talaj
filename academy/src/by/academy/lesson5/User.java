package by.academy.lesson5;

public class User {

	String userName;
	
	public User() {
		super();
	}
	public User(String userName) {
		super();
		this.userName=userName;
	}
	
	public void setUserName(String userName) {
		this.userName=userName;
	}
	
	public String getUserName() {
		return userName;
	}
	
}
